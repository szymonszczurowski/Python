f_smaller = 3.141592653358973
f_bigger = 3.87756392764

import math

print('Najmniejsza liczba całkowita większa od podanej liczby:', math.ceil(f_smaller), math.ceil(f_bigger))

print('Największa liczba całkowita mniejsza od podanej liczby, czyli zaokrąglenie w dół:', math.floor(f_smaller), math.floor(f_bigger))

print('Najmniejsza liczba  całkowita większa od podanej liczby:', math.ceil(-f_smaller), math.ceil(-f_bigger))

print('Największa liczba całkowita mniejsza od podanej liczbył:', math.floor(-f_smaller), math.floor(-f_bigger))

print('Potęga i pierwiastek:', math.pow(2,8), math.pow(9,0.5)) #pierwieastek z 9 to 9^1/2

print('pierwiastek kwadratowy:', math.sqrt(16))

print(math.pi, math.e)

print('funckje trygonometryczne:', math.sin(math.pi/2), math.cos(math.pi/4))  #wartość funckji sisnus dla pi/2, tak samo dla cos

